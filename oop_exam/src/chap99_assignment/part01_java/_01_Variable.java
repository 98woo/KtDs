package chap99_assignment.part01_java;

public class _01_Variable {

    /*
        1. 변수 활용 문제
           정수형 변수 2개를 선언하고 값을 할당해본다.
     */
    public static void main(String[] args) {
        int a = 10;
        long b = 30_000_000_000L;

        System.out.println("int a = " + a);
        System.out.println("long b = " + b);
    }
}
